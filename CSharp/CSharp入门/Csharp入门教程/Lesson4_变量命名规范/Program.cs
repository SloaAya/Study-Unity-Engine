using System;

namespace Lesson4_变量命名规范
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("变量命名规则");
            //驼峰命名法——首字母小写，之后单词首字母大写（变量）
            string myName = "某人";
            string yourName = "某某人";
            string yourMotherName = "某某某人";
            //帕斯卡命名法——所有单词首字母都大写（函数、类）

        }   
    }
}